public class YearsBeforeRetirement
{
  public static void main(String[] args)
  { 
    int AgeNow;
    int AgeRetirement;
    int YearsUntilRetirement;
    AgeNow = 18;
    AgeRetirement = 68;
    YearsUntilRetirement = AgeRetirement - AgeNow;
    System.out.println("My age now is " + AgeNow);
    System.out.println("I will retire at 68");
    System.out.println("Years left working is " + YearsUntilRetirement);
  }
}
